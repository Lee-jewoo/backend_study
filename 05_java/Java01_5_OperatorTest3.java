package exam02;

public class Java01_5_OperatorTest3 {

	public static void main(String[] args) {
		int n = 10;
		int m = 3;
		// 비교 연산자
		System.out.println(n == m); // false
		System.out.println(n > m); // true
		System.out.println(n >= m); // true
		System.out.println(n < m); // false
		System.out.println(n <= m); // false
		System.out.println(n != m); // true

	}

}
